// ── Hilfsfunktionen ──────────────────────────────────────────────

export function getItemsForSlot(slot: PlannerSlot, classId?: string): PlannerItem[] {
  return plannerItems.filter((item) => {
    if (item.slot !== slot) return false;
    if (classId && item.classIds && !item.classIds.includes(classId)) return false;
    return true;
  });
}

export function getItemById(id: string): PlannerItem | undefined {
  return plannerItems.find((item) => item.id === id);
}

export function getSlotsForClass(classId: string): PlannerSlot[] {
  const usedSlots = new Set<PlannerSlot>();
  plannerItems.forEach((item) => {
    if (!item.classIds || item.classIds.includes(classId)) {
      usedSlots.add(item.slot);
    }
  });
  return Array.from(usedSlots);
}
